package productdetails;


public class product {
	private int pid;
	private String pname;
	private int qty;
	public int getqty() {
		return qty;
	}
	public void setqty(int qty) {
		this.qty = qty;
	}
	public int getpid() {
		return pid;
	}
	public void setpid(int pid) {
		this.pid = pid;
	}
	public String getpname() {
		return pname;
	}
	public void setpname(String pname) {
		this.pname = pname;
	}
	
}